#include<stdio.h>
int main(void) {
    // create variables
    int myNum = 5; // integer
    float myFloatNum = 5.99; // floating point number
    char myLetter = 'D'; // string
    // High precision floating point data or numbers
    double myDouble = 3.2325467;
    // print output variables
    printf("%d\n", myNum);
    printf("%f\n", myFloatNum);
    printf("%c\n", myLetter);
    printf("%lf\n", myDouble);
}