#!/bin/bash

# Input and Output Example

# Prompt the user to enter their name
echo "Hello! Please enter your name:"

# Read user input into a variable
read name

# Display a greeting message
echo "Hello, $name! Welcome to the world of Bash scripting."

# Prompt the user to enter their age
echo "Could you also tell us your age, $name?"

# Read user input for age
read age

# Display a message with the user's name and age
echo "Great, $name! So you're $age years old."
